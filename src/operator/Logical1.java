package operator;

public class Logical1 {
    public static void main(String[] args) {
        System.out.println("&&: and 연산자");
        System.out.println( true && false);
        System.out.println( true && true);

        System.out.println("|| : or 연산자");
        System.out.println(true || false);
        System.out.println(false || false);
    }
}
